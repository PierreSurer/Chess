const int values[9] = {20000, 900, 330, 320, 500, 100, 20000, 500, 100};

int evaluation(int team, char* board) {
  int res = 0;
  for(int i = 0; i < 64; i++) {
    if(board[i] == 0) continue;
      int piece = abs(board[i]) - 1;
    if(board[i] < 0) {
        res += -values(piece) - heatmap[piece][64-i];
    }else {
        res += values(piece) + heatmap[piece][i];
    }
  }
  res *= team;
  return res;
}